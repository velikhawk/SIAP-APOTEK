package sistem_akuntansi.entity;

/**
 *
 * @author VELIK
 */
public class Pbf {
    private String kode_pbf;
    private String nama_pbf;
    private String alamat;
    private String telepon;
    private String kota;

    public String getKode_pbf() {
        return kode_pbf;
    }

    public void setKode_pbf(String kode_pbf) {
        this.kode_pbf = kode_pbf;
    }

    public String getNama_pbf() {
        return nama_pbf;
    }

    public void setNama_pbf(String nama_pbf) {
        this.nama_pbf = nama_pbf;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTelepon() {
        return telepon;
    }

    public void setTelepon(String telepon) {
        this.telepon = telepon;
    }

    public String getKota() {
        return kota;
    }

    public void setKota(String kota) {
        this.kota = kota;
    }
    
}
