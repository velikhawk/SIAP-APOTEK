    private void buttonSisipkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSisipkanActionPerformed
        // TODO add your handling code here:
        Perkiraan perkiraan = perkiraanData.getDataSelection();        
        
        KasMasukDetail kasMasukDetail = new KasMasukDetail();
        kasMasukDetail.setKode_rekening(textKodePenerimaan.getText());
        kasMasukDetail.setJumlah(Integer.parseInt(textJumlah.getValue().toString()));
        kasMasukDetail.setHeader_nomor(textNomor.getText());
        kasMasukDetail.setPerkiraan(perkiraan);
               
        
        Vector<KasMasukDetail> kasMasukDetails = kasMasukDetailTableModel.getData();
        boolean baru = true;
                
        for (KasMasukDetail kasMasukDetailOld : kasMasukDetails) {
            if (kasMasukDetailOld.getKode_rekening().equals(textKodePenerimaan.getText())) {
                baru = false;
                kasMasukDetailOld.setJumlah(kasMasukDetailOld.getJumlah() + Integer.parseInt(textJumlah.getValue().toString()));
                break;
            }
        }        
        
        if (baru) kasMasukDetailTableModel.getData().add(kasMasukDetail);
        kasMasukDetailTableModel.fireTableDataChanged();
        
        textKodePenerimaan.setText(null);
        textNamaPenerimaan.setText(null);
        textJumlah.setValue(0);
        textTotal.setValue(getTotal());
    }//GEN-LAST:event_buttonSisipkanActionPerformed

    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        // TODO add your handling code here:
        int index = tabelPenerimaan.getSelectedRow();
        if(index < 0){
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
        }else{            
            kasMasukDetailTableModel.getData().remove(tabelPenerimaan.convertRowIndexToModel(index));
            kasMasukDetailTableModel.fireTableDataChanged();
            
            textKodePenerimaan.setText(null);
            textNamaPenerimaan.setText(null);
            textJumlah.setValue(0);
            textTotal.setValue(getTotal());
        }
    }//GEN-LAST:event_buttonHapusActionPerformed

    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:         
        try {
            connection.setAutoCommit(false);
            KasMasuk kasMasuk = new KasMasuk();
            kasMasuk.setNomor(textNomor.getText());
            kasMasuk.setTanggal((Date) textTanggal.getValue());
            kasMasuk.setKeterangan(textKeterangan.getText());
            kasMasuk.setKode_rekening_kas(textKodeKas.getText());
            kasMasuk.setTotal(Integer.parseInt(textTotal.getValue().toString()));
            kasMasukModel.insert(kasMasuk);
            
            //jurnal debet
            JurnalTransaksi jurnalDebet = new JurnalTransaksi();
            jurnalDebet.setTanggal(kasMasuk.getTanggal());
            jurnalDebet.setNomor_bukti(kasMasuk.getNomor());
            jurnalDebet.setKeterangan(kasMasuk.getKeterangan());
            jurnalDebet.setKode_rekening(kasMasuk.getKode_rekening_kas());
            jurnalDebet.setTipe("JKM");
            jurnalDebet.setDebet(kasMasuk.getTotal());
            jurnalDebet.setKredit(0);
            jurnalTransaksiModel.insert(jurnalDebet); 
            
            
            for (KasMasukDetail dataTable : kasMasukDetailTableModel.getData()) {
                kasMasukDetailModel.insert(dataTable);
                
                //jurnal kredit
                JurnalTransaksi jurnalKredit = new JurnalTransaksi();
                jurnalKredit.setTanggal(kasMasuk.getTanggal());
                jurnalKredit.setNomor_bukti(kasMasuk.getNomor());
                jurnalKredit.setKeterangan(kasMasuk.getKeterangan());
                jurnalKredit.setKode_rekening(dataTable.getKode_rekening());
                jurnalKredit.setTipe("JKM");
                jurnalKredit.setDebet(0);
                jurnalKredit.setKredit(dataTable.getJumlah());
                jurnalTransaksiModel.insert(jurnalKredit); 
            }
            
            connection.commit();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);
           
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Data gagal disimpan!", "INFORMASI", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(KasMasukForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_buttonSimpanActionPerformed

